#include <iostream>
#include <string>
using namespace std;

string text[] = {"I", "s", "a", "b", "e", "l", "l", "a"};

int main (){

    int i;
    while (i<text.size){
        text[i];
        i++;
    }

    cout << "The length of the string is" << i << "." <<endl;

    return 0;
}
